package data.dto;

public class ScheduleDto {
	
	private String num;
	private String shop_num;
	private String stime;
	private int room;
	private int hall;
	private int room_cnt;
	private int hall_cnt;
	
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getShop_num() {
		return shop_num;
	}
	public void setShop_num(String shop_num) {
		this.shop_num = shop_num;
	}
	public String getStime() {
		return stime;
	}
	public void setStime(String stime) {
		this.stime = stime;
	}
	public int getRoom() {
		return room;
	}
	public void setRoom(int room) {
		this.room = room;
	}
	public int getHall() {
		return hall;
	}
	public void setHall(int hall) {
		this.hall = hall;
	}
	public int getRoom_cnt() {
		return room_cnt;
	}
	public void setRoom_cnt(int room_cnt) {
		this.room_cnt = room_cnt;
	}
	public int getHall_cnt() {
		return hall_cnt;
	}
	public void setHall_cnt(int hall_cnt) {
		this.hall_cnt = hall_cnt;
	}
	
	

}
